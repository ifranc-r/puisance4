#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		len;
	int		n;

	n = 0;
	len = ft_strlen(s);
	while (len--)
	{
		f(n, s);
		s++;
		n++;
	}
}
